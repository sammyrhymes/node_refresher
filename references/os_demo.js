const os = require('os');

// Platform
console.log(os.platform());

// CPU Architecture
console.log(os.arch());


// CPU info
console.log(os.cpus());


// free memory
console.log(os.freemem());


// Total memory
console.log(os.totalmem());

// home dir
console.log(os.homedir());

// uptime
console.log(os.uptime());





